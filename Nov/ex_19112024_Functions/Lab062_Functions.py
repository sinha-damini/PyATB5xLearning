#Basic Function

#Part 1 -> Defining a function.
def greet():
    print('Hello, Welcome to Python!')
# If we run this, it will not run as only defining is done here and not calling.


#Part 2 -> Calling a function.
def greetings():
    print('Hello, Welcome to Python!')
greetings()


#We can call a same function multiple number of times.
def new_greetings():
    print('Hello, Welcome to Python!. Hope you will enjoy')

new_greetings()
new_greetings()
print("Hope you are doing good")
new_greetings()

